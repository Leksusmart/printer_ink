/**
 * НАЗНАЧЕНИЕ ФАЙЛА:
 * Это файл модульных (Unit) тестов для `AppController`.
 * Он нужен для автоматической проверки корректности работы контроллера в изолированной среде.
 * С помощью утилит NestJS здесь создается "игрушечный" (тестовый) модуль, который имитирует 
 * работу реального приложения, позволяя быстро проверять ответы методов без запуска всего сервера.
 */

// Импортируем инструменты тестирования из специального пакета NestJS
import { Test, TestingModule } from '@nestjs/testing';
// Импортируем контроллер, который мы собираемся тестировать
import { AppController } from './app.controller';
// Импортируем сервис, который необходим контроллеру для работы
import { AppService } from './app.service';

// Главный блок тест-кейса (использует фреймворк Jest), объединяющий все тесты для AppController
describe('AppController', () => {
  // Объявляем переменную, в которую запишем экземпляр контроллера перед каждым тестом
  let appController: AppController;

  // Хук beforeEach выполняется автоматически перед КАЖДЫМ отдельным тестом `it`
  beforeEach(async () => {
    // Создаем изолированный тестовый модуль, симулируя структуру реального AppModule
    const app: TestingModule = await Test.createTestingModule({
      controllers: [AppController], // Регистрируем тестируемый контроллер
      providers: [AppService],      // Регистрируем его зависимости
    }).compile(); // Компилируем модуль для тестирования

    // Достаем готовый экземпляр контроллера из тестового контейнера NestJS
    appController = app.get<AppController>(AppController);
  });

  // Вложенный блок для группировки тестов конкретного функционала (в данном случае корневого роута)
  describe('root', () => {
    // Конкретный тест-кейс, описывающий ожидаемое поведение
    it('should return "Hello World!"', () => {
      // Вызываем метод контроллера и проверяем (expect), что результат строго равен 'Hello World!'
      expect(appController.getHello()).toBe('Hello World!');
    });
  });
});
